package com.example.sampleconstraintlayout;

public class ClassNama {
    private String Nama;

    //Inisialisasi objek
    public ClassNama(String nama)
    {
        this.Nama = nama;
    }
    //membuat method getName untuk mengembalikan nilai ke variabel nama
    public String getName() {
        return this.Nama;
    }
}
